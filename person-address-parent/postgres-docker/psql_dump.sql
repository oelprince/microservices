CREATE SCHEMA PERSON;

CREATE TABLE PERSON.PERSON
(
   ID char(36) PRIMARY KEY NOT NULL,
   ADDRESS_ID char(36),
   FIRST_NAME varchar(120) NOT NULL,
   MID_NAME varchar(60),
   LAST_NAME varchar(120) NOT NULL
)
;

CREATE SCHEMA ADDRESS;

CREATE TABLE ADDRESS.ADDRESS
(
   ID char(36) PRIMARY KEY NOT NULL,
   PERSON_ID char(36),
   ADDRESS_LINE1 varchar(225) NOT NULL,
   ADDRESS_LINE2 varchar(225),
   is_current char(1) DEFAULT 'N'::bpchar NOT NULL
)
;

CREATE SCHEMA CREDIT;

CREATE TABLE CREDIT.CREDIT
(
    ID char(36) PRIMARY KEY NOT NULL,
    AUTH_NUM char(10) NOT NULL,
    AUTH_DATE date NOT NULL,
    SCORE int NOT NULL,
    PERSON_ID char(36)
)
;

CREATE SCHEMA PERSON_ADDRESS;

CREATE TABLE PERSON_ADDRESS.PERSON_ADDRESS
(
	person_id char(36),
	address_id char(36)
);




